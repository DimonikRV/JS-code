function getParsedIntegers(arr) {
  return arr.map((num) => Number.parseInt(num));
}
function getParsedIntegersV2(arr) {
  return arr.map((num) => parseInt(num));
}
function getParsedFloats(arr) {
  return arr.map((num) => Number.parseFloat(num));
}
